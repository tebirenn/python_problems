#---------- Case D ----------#

a = float(input("Первое число: "))   # Ввод первого числа в типе float
op = input("Ар. операция: ")         # Ввод ариф. операций в типе str
b = float(input("Второе число: "))   # Ввод второго числа в типе float

print("\nРезультат: ")
# По нужным операторам, выдаем нужный результат
if op == "+":
    print(a, op, b, "=", a+b)
elif op == "-":
    print(a, op, b, "=", a-b)
elif op == "*":
    print(a, op, b, "=", a*b)
elif op == "**":
    print(a, op, b, "=", a**b)
# Прежде чем делить число, нужно проверить деление на ноль
elif (op == "/" or op == "//" or op == "%") and b == 0:
# elif op in "//%" and b == 0:   # альтернативное условие
    print("Делить на ноль нельзя!!!")
elif op == "/":
    print(a, op, b, "=", a/b)
elif op == "//":
    print(a, op, b, "=", a//b)
elif op == "%":
    print(a, op, b, "=", a%b)
else:
    print("неопознанная операция")