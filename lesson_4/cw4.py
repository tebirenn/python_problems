# для подключения функций randint
from random import randint


#---------- Case A ----------#

# Начальные принты 
print("Угадай число!")
print("Загаданное число находиться между 1 и 6")
print("У вас есть 3 попытки")
print("Игра ничинается...\n")

x = randint(1, 6)   # Компьютер загадывает число от 1 до 6

ans = int(input("Попытка #1. Введите число: "))  # Ввод первой попытки

# Если загаданное число и введенное число равны, то пользователь угадал
if ans == x:
    print("Вы угадали!")
# Иначе нужно переходить ко второй попытке
else:
    print("Вы не угадали, у вас еще 2 попытки!\n")

    ans = int(input("Попытка #2. Введите число: ")) # Ввод второй попытки
    if ans == x:
        print("Вы угадали!")
    # Если не угадал, переходим к третьей попытке
    else:
        print("Вы не угадали, у вас еще 1 попытка!\n")

        ans = int(input("Попытка #3. Введите число: ")) # Ввод третьей попытки
        if ans == x:
            print("Вы угадали!")
        # Если не угадал, заканчиваем игру
        else:
            print("Вы програли, загаданное число:", x)





