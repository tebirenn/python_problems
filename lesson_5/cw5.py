### Easy B ###

number = int(input("Введите число: "))    
summa = 0                                 # Для суммы от 1 до number 

i = 1             # i начинается с 1
while i <= number:     # Пока не превышает number
    summa += i    # в сумму добавляется каждое значение i от 1 до number
    i += 1        # i каждый раз растет на 1, чтобы рассмотреть все числа до number

print("Сумма от 1 до", number, "=", summa)


