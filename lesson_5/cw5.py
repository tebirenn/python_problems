### Hard B ###

summa = 0          # Для суммы чисел
summa_square = 0   # Для суммы квадратов чисел

while True:        
    number = int(input())       # С каждым разом принимаем число

    summa += number             # В summa добавляем само число
    summa_square += number**2   # В summa_square добавляем квадрат числа

    if summa == 0:              # Если сумма чисел будет 0, больше чисел принимать не надо
        break                   # останавливаем цикл

print("Сумма квадратов:", summa_square)




