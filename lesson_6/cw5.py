# Начальные принты чтобы обьяснить пользователю об мини-проекте
print("Камень-ножницы-бумага!")
print("Вы будете играть с компьютером в игру \"су-ли-фа\".")
print("Компьютер сделал уже свой выбор")
print("Теперь ваша очередь...\n")

print("1---Камень\n2---Ножницы\n3---Бумага\n")

cnt_human = 0          # Для количества побед человека 
cnt_comp = 0           # Для количества побед компа 

while True:
    print("-" * 35)    # Длинная линия
    x = randint(1, 3)  # Рандомный выбор компа от 1 по 3
    answer = int(input("Сделайте свой выбор (1|2|3): "))    # Выбор человека

    print()            # Для отступа

    # Ищем что выбрал компьютер
    if x == 1:
        print("Выбор компьютера: Камень")
    elif x == 2:
        print("Выбор компьютера: Ножницы")
    else:
        print("Выбор компьютера: Бумага")

    # Ищем что выбрал человек
    if answer == 1:
        print("Ваш выбор: Камень")
    elif answer == 2:
        print("Ваш выбор: Ножницы")
    else:
        print("Ваш выбор: Бумага")

    # Выборы одинаковые, значит ничья
    if x == answer:
        print("Ничья")

    # Проверяем три случая когда выиграывает компьютер
    elif (x == 1 and answer == 2) or (x == 2 and answer == 3) or (x == 3 and answer == 1):
        cnt_comp += 1      # Если одно из них случилось  +1 победа компьютеру

    # Во всех остальных случаях победа человка
    else:
        cnt_human += 1     # +1 победа человеку

    # В каждой игре независимо от исхода выводим счёт
    print("Счет(Компьютер: " + str(cnt_comp) + ", Человек: " + str(cnt_human) + ")\n")

    print()      # для отступа

    if cnt_human == 3:               # Если человек первым дошел до трех побед
        print("Вы выиграли игру!")   # Он победил игру
        break                        # Оставновка цикла

    elif cnt_comp == 3:              # Тоже самое для компа
        print("Компьютер выиграл игру!")   
        break





