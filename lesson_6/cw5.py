# Начальные принты чтобы обьяснить пользователю об мини-проекте
print("Шарик с предсказаниями!")
print("Каждый раз шарик будет выдавать случайное предсказание.")
print("А так же можете задать свой вопрос и получить ответ 'Да' или 'Нет'.\n")
print("1) Предсказание\n2) Задать вопрос\n")

# Цикл чтобы пользователь мог несколько раз опробовать шарик
while True:
    human_choice = int(input("Выберите режим: "))     

    # Если режим первый
    if human_choice == 1:    
        x = randint(1, 4)                       # Рандомно берем число от 1 по 4
        # Проверяем какое число упало и выдаем под них предсказание 
        if x == 1:
            print("Бдует плохаяа погода")
        elif x == 2:
            print("У вас будет удачный день")
        elif x == 3:
            print("Вам кто-то навредит!")
        else:
            print("Будет прибыльный день!")

    # Если режим втотой
    elif human_choice == 2:
        input("Задайте свой вопрос: ")      # Человек задает вопрос
        x = randint(1, 2)                   # Рандомный выбор 1 или 2
        if x == 1:                          # Если 1 отвечаем ДА
            print("Ответ: Да")
        else:                               # Иначе НЕТ
            print("Ответ: Нет")

    # Если человек пытался выбрать другой режим
    else:
        print("Вы сделали некорректный выбор!")

    print()  # для отступа 
    ans = input("Хотите продолжить? (y/n): ")  
    if ans in "noNonONO":        # Если ответ человка есть внутри строки состоящий из No      
        print("Goodbye!")        
        break                    # Завершаем шарик

    print()         # Для отступа


