from random import randint




### Problem A ###

# Начальные принты чтобы обьяснить пользователю об мини-проекте
print("Угадай число!")
print("Загаданное число находится между 1 и 100.")
print("У вас неограноченное колчество попыток")
print("Игра начинается...\n")

x = randint(1, 100)              # Компьютер выдает случайное число от 1 по 100
cnt = 0                          # Сюда будем считать количество попыток

# Бесконечный цикл пока пользователь не угадает
while True:
    answer = int(input("Попытайся угадать: "))   
    cnt += 1                     # Только что пользователь сделал еще одну попытку

    if answer == x:              # Если число пользователя и загаданное число равны
        print("Вы угадали!")     # Он угадал
        print("Количество попыток:", cnt)
        break                    # Завершаем игру

    elif x > answer:                                # Иначе-если загаданное число больше
        print("Вы не угадали, попробуйте еще!")     # Сообщаем пользователю
        print("Берите выше!")                       # Даем подсказку

    else:                                           # Иначе загаданное число ниже
        print("Вы не угадали, попробуйте еще!")     # Сообщаем пользователю
        print("Берите ниже!")                       # Даем подсказку

    print() #   Делаем отступ, а затем цикл будет перезапущен для следующей попытки





