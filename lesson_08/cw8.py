first = 0  # Первое число фибоначчи это: 0
second = 1 # Второе число фибоначчи это: 1
# Остальные числа можно получить суммировав последние два числа 
# Например №3: 0 + 1 = 1
#          №4: 1 + 1 = 2
#          №5: 2 + 1 = 3
#          №6: 2 + 3 = 5
#          №7: 3 + 5 = 8 и тд.

n = int(input("n: "))

if n == 1:
    print(first)
elif n == 2:
    print(second)
else:
    # else чтобы найти числа фибоначчи кроме 1 и 2(Мы их изначальное знаем)

    for i in range(n-2):                # Цикл запускается на два круга меньше чем n. Опять же потому что первые два числа нам уже известны
        next_fib = first + second       # Как говорили новое число получим суммой двух последних чисел
        first = second                  # Далее сдвигаем оба числа на одну позицию вперед: first к second
        second = next_fib               # А second к next_fib
    
    print(next_fib)