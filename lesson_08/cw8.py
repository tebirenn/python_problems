#---------- Hard B ----------#

a = int(input("Начало: "))
b = int(input("Конец:  "))

for num in range(a, b+1):           # Рассматриваем все числа от a до b

    cnt = 0             # Для количества делителей числа

    # Нужно посчитать количество делителей числа num
    for div in range(1, num+1):      # Нужно рассмотреть все числа от 1 по num

        if num % div == 0:           # Если num делится на div
            cnt += 1                 # Плюс в количество 

    # У простых чисел должно быть только два делителя(1 и само число)
    # Так же простые числа начинается с 2
    if cnt == 2 and num >= 2:
        print(num, end=" ")




